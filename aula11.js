// Crie uma variável com o sinal - para separar;
// Separe todos os dados da variável criando um vetor;
// Após isso, pegue somente os 2 primeiros dados dessa variável;

let nome = "Jennyffer-Monteiro-de-Lima";        // nao pode ser dentro de um Array [ ]
console.log(nome);      // mostrará a variavel completa
